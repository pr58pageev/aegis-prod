<h2>1. Главная - Личный Кабинет </h2>
 Страница Главная отображает следующие данные:<br>
 <br>
 1. Cостояние Заказов - Номер заказ, сумма, дата и ссылка на подробное содержание заказа&nbsp;<br>
 2. Организации - Перечень организаций, на которые можно оформить заказ&nbsp;<br>
 3. Отложенные товары - товары, которые не добавлены в заказ, но которые вы рассматриваете для заказа<br>
 4. Избранные сслыки - можете сохранить здесь ссылки на товары или любые другие страницы<br>
 5. Персональные данные - отображение персональных данных сотрудника, которые передаются менеджеру в заказе.<br>
 <br>
<h2>2. Создание заказа</h2>
 &nbsp;<a name="sozdaniezakaza"></a><br>
 <br>
 <img width="1024" alt="Создание Заказа - разделы и фильтры" src="/upload/medialibrary/ced/ceda35ba3611a4760b722aaf1031a5db.jpg" height="553" title="Создание Заказа - разделы и фильтры"><br>
 <br>
 <br>
 Для поиска товаров вы можете воспользоваться выбором товаров из категорий каталога и фильтром по свойствам товаров.<br>
 Каждый фильтр категорий товаров имеет свой индивидуальный набор свойств для поиска. Поэтому сначала выберите категории, а потом фильтр, если это необходимо.<br>
 <br>
 <img width="1024" alt="Поиск товара и добавление в Корзину" src="/upload/medialibrary/108/108ea060ed906cbc3b464c1010d78cdb.jpg" height="563" title="Поиск товара и добавление в Корзину"><br>
 <br>
 <br>
 Поиск товара осуществляется по нашему внутреннему Коду товара, Артикулу, Названию или Производителю. Вы можете ввести часть этих данных и алгоритм подберет наиболее релевантные товары по вашему запросу.<br>
 <br>
<h2>3. Оформление Заказа&nbsp;<a name="oformlenie"></a></h2>
 <br>
 <img width="1023" alt="Оформление Заказа" src="/upload/medialibrary/756/756411e45cf78defdff6130eb549b0d8.jpg" height="540" title="Оформление Заказа"><br>
 <br>
 На этапе оформления заказа вы можете скорректировать свою Корзину, Добавив или удалив из нее товары.<br>
 <br>
 Заполнение данных заказа&nbsp;&nbsp;<br>
 <br>
 <img width="1024" alt="Оформление заказа - Юридическое лицо и Доставка" src="/upload/medialibrary/ba7/ba72eebf4077a35ab0d41a610218eee2.jpg" height="770" title="Оформление заказа - Юридическое лицо и Доставка"><br>
 <span><br>
 3.1. Заполнение данных юридического лица. Вы можете оформить заказ на Индивидуального предпринимателя или Юридическое лицо. Для этого нужно нажать на ссылку "Создать юридическое лицо / ИП" и заполнить данные. Наименование и ИНН. Если сначала ввести ИНН или его часть, то система автоматически находит Наименование.<br>
 <br>
 3.2. Выбираем способ доставки<br>
 <br>
 - Самовывоз осуществляется с нашего склада по адресу ул. Ботаническая д.10Б&nbsp;<br>
 - Доставка:&nbsp;<br>
 <br>
 - Введите адрес доставки, включая город. На сайте доступно оформление доставки по Москве и Московской области. При этом стоимость доставки рассчитывается автоматически исходя из веса заказа и адреса заказа.<br>
 - Укажите желаемую дату доставки<br>
 - Укажите желаемый временной интервал доставки<br>
 - Укажите Имя и телефон лица, принимающего доставку&nbsp;<br>
 <br>
 3.3. Заполните Комментарий к доставке если это необходимо<br>
 <br>
 3.4. Для оформления заказа нажмите кнопку Оформить заказ. Вы можете сохранить заказ в статусе Черновик и продолжить с ним работу позже - добавить или удалить какие-то товары.&nbsp; &nbsp;<br>
 <br>
<h2>4. Состояние заказа&nbsp;<a name="sostoyanie"></a></h2>
 </span><span>Для того, чтобы увидеть полную информацию по заказу необходимо перейти на страницу Состояние заказов и нажать на номер Заказа.&nbsp;<br>
 На странице полной информации по заказу доступны функции:<br>
 <br>
 <img width="1024" alt="Отмена заказа" src="/upload/medialibrary/c63/c63025f0be20b22d8e3a8576292e9487.jpg" height="615" title="Отмена заказа"><br>
 <br>
 1. Повторить заказ<br>
 Это функция копирования заказа с возможностью изменить все его параметры. Перечень и количество товаров, данные доставки, юридическое лицо.&nbsp;&nbsp;<br>
 <br>
 2. Печать&nbsp;<br>
 По нажатию на кнопку вам доступна печатная форма заказа.&nbsp;<br>
 <br>
 3. Отмена<br>
 Если заказ находится в состоянии Ожидает подтверждения, то вы можете его отменить.&nbsp;&nbsp;<br>
 <br>
 <br>
