<h2>7. Индивидуальные товары</h2>
 &nbsp;<a name="individtovary"></a><br>
 </span><img width="1024" alt="Индивидуальные товары" src="/upload/medialibrary/8fd/8fd82b69f10e461035649bd4f009f13d.jpg" height="671" title="Индивидуальные товары"><span><br>
 <br>
 Индивидуальные товары - это товары, которые не доступны в публичной части сайта и при этом присутствуют в заказах организаций, на которые вы ранее оформляли заказы. Если вашей организации предоставляются специальные цены на товары, отличные от базовой цены, то список таких товаров и цены на них так же отражаются в разделе индивидуальные товары.&nbsp;<br>
 Для отображения списка товаров выберите организацию из выпадающего списка Организации. Так же дополнительно вы можете воспользоваться фильтром категорий товаров, нажав на поле - Товары в разделах.<br>
 Далее работа с товарами аналогична работе с товарами при оформлении заказа. При этом необходимо учитывать, что выбрав индивидуальный товар для конкретной организации, вы не сможете поменять эту организацию на этапе оформления заказа.&nbsp;&nbsp;<br>
 <br>
